"""Профиль релевантности архбюро: ключевые слова (+вес) и анти-слова (-вес).

Фильтруем по тексту ПРЕДМЕТА закупки (колонка «Название»), т.к. ОКПД2 в выгрузке
Контура нет. Веса подобраны эвристически — это первая итерация, аналитик правит
списки тут же. Стемы (напр. «проектн») ловят все падежи: проектная/проектную/...

Короткие/неоднозначные токены задаём как regex с границей слова (`\\bпир\\b`),
чтобы не цеплять «пирс», «пирог» и т.п.
"""

from __future__ import annotations

import re
from typing import NamedTuple


class Keyword(NamedTuple):
    label: str  # человекочитаемая причина
    pattern: re.Pattern[str]  # по чему ищем
    weight: int  # вклад в score (анти-слова — отрицательный)


def _stem(label: str, stem: str, weight: int) -> Keyword:
    """Стем как подстрока (морфология русского): «проектн» -> проектная/проектной/..."""
    return Keyword(label, re.compile(re.escape(stem), re.IGNORECASE), weight)


def _word(label: str, word: str, weight: int) -> Keyword:
    """Короткий токен с границей слова, чтобы не ловить случайные вхождения."""
    return Keyword(label, re.compile(rf"\b{re.escape(word)}\b", re.IGNORECASE), weight)


