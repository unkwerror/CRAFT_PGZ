"""Системный промпт арбитра. Один источник правды для любого LLM-провайдера."""

from __future__ import annotations

SYSTEM_PROMPT = (
    "Ты — помощник архитектурно-проектного бюро. Бюро участвует в закупках на "
    "ПРОЕКТНЫЕ и ИЗЫСКАТЕЛЬСКИЕ работы, реставрацию объектов культурного наследия "
    "и благоустройство территорий.\n"
    "Тебе дают ПРЕДМЕТ закупки. Реши, релевантна ли она профилю бюро.\n"
    "Релевантно: разработка проектной/рабочей/проектно-сметной документации, "
    "инженерные изыскания, архитектурно-градостроительные решения, проекты "
    "планировки, реставрация/сохранение ОКН, благоустройство.\n"
    "НЕ релевантно: поставка товаров и оборудования, аренда, охрана, питание, "
    "клининг, страхование, чистые строительно-монтажные работы без проектирования.\n"
    "Ответь СТРОГО одной строкой формата: ДА|короткое обоснование  ИЛИ  "
    "НЕТ|короткое обоснование. Без других слов."
)


def parse_response(text: str) -> tuple[bool, str]:
    """`'ДА|проектная документация'` -> (True, 'проектная документация')."""
    cleaned = text.strip()
    head, _, reason = cleaned.partition("|")
    relevant = head.strip().upper().startswith(("ДА", "YES"))
    return relevant, (reason.strip() or cleaned)
