# Structured output: {results: [{reestr_number, score, summary}, ...]} — по одной
# записи на карточку (карточки шлём пачкой в одном запросе).
BATCH_SCHEMA: dict[str, Any] = {
    "type": "object",
    "properties": {
        "results": {
            "type": "array",
            "items": {
                "type": "object",
                "properties": {
                    "reestr_number": {"type": "string"},
                    "score": {"type": "integer"},
                    "summary": {"type": "string"},
                },
                "required": ["reestr_number", "score", "summary"],
                "additionalProperties": False,
            },
        }
    },
    "required": ["results"],
    "additionalProperties": False,
}


def _date(value: dt.datetime | None) -> str:
    return value.strftime("%d.%m.%Y") if value is not None else "—"


def _sec(securities: dict[str, Any] | None, key: str) -> str:
    if not securities:
        return "—"
    item = securities.get(key) or {}
    return str(item.get("raw") or "—")


def build_card_block(t: Any) -> str:
    """Собрать все поля карточки в текстовый блок для Claude."""
    lines = [
        f"Предмет: {t.subject or '—'}",
        f"НМЦ: {t.nmck if t.nmck is not None else '—'} {t.currency or ''}".rstrip(),
        f"Тип торгов (закон): {t.law or '—'}",
        f"Способ отбора: {t.purchase_method or '—'}",
        f"Этап: {t.stage or '—'}",
        f"Аванс: {t.advance_raw or 'нет'}",
        f"Обеспечение заявки: {_sec(t.securities, 'bid')}; "
        f"контракта: {_sec(t.securities, 'contract')}",
        f"Опубликовано: {_date(t.publish_date)}; окончание подачи заявок: "
        f"{_date(t.submission_deadline)}",
        f"Регион заказчика: {(t.region_code or '')} {(t.region_name or '—')}".strip(),
        f"Заказчик: {t.customer_name or '—'} (ИНН {t.customer_inn or '—'})",
        f"Место поставки: {t.delivery_place or '—'}",
        f"ЭТП: {t.etp or '—'}; СМП/СОНО: {t.smp_sono or 'нет'}",
    ]
    return "\n".join(lines)


def build_batch_message(items: list[tuple[str, str]]) -> str:
    """Собрать пачку карточек [(reestr_number, card_text)] в одно сообщение."""
    header = (
        "Оцени КАЖДУЮ из закупок ниже. Верни объект с полем results — массив, по одному "
        "элементу на каждую закупку. Поле reestr_number копируй ТОЧНО из заголовка "
        "«=== Тендер X ===».\n"
    )
    blocks = [f"=== Тендер {reestr} ===\n{card}" for reestr, card in items]
    return header + "\n\n" + "\n\n".join(blocks)
