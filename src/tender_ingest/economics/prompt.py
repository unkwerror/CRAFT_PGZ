"""Промпт и схема рекомендации ИИ-экономиста.

Ключевое правило: модель рассуждает ТОЛЬКО над предоставленными фактами (SQL-агрегаты
сегмента + похожие кейсы с исходами и заметками бюро) — цифры не выдумываются, каждая
проверяема по кейсам. Порядок полей схемы = порядок генерации: rationale и risks идут
ДО вывода, чтобы модель рассуждала перед рекомендацией.
"""

from __future__ import annotations

from typing import Any

from tender_ingest.company import COMPANY_PROFILE

SYSTEM_PROMPT = (
    "Ты — экономист-аналитик тендеров проектного бюро «КРАФТ ГРУПП» (craft72.ru).\n\n"
    "О КОМПАНИИ. " + COMPANY_PROFILE + "\n\n"
    """ЗАДАЧА. По карточке закупки, фактам по сегменту и похожим кейсам из истории бюро \
дай рекомендацию: участвовать ли и с какой ценовой стратегией.

ЖЁСТКИЕ ПРАВИЛА РАБОТЫ С ЦИФРАМИ:
- Опирайся ТОЛЬКО на предоставленные факты (агрегаты сегмента, кейсы, заметки бюро). \
НЕ выдумывай статистику и «средние по рынку», которых нет во входных данных.
- Каждую цифру в выводе привязывай к источнику: «по N нашим торгам в сегменте…», \
«в кейсе {номер} снижение было…».
- Если данных мало (нет наших участий в сегменте, мало кейсов) — скажи это прямо в \
data_notes, снизь confidence и давай осторожную рекомендацию от рыночных фактов \
(медианная НМЦ, сроки, аванс).
- Заметки бюро — знание людей, им доверяй больше формальных полей (например, про \
поведение конкретного заказчика).
- Если есть контрпримеры (прошлые рекомендации с оценкой «мимо») — учти, в чём была \
ошибка, и не повторяй её.

ЧТО ВЫДАТЬ (поля схемы):
- rationale — рассуждение: сегмент, что говорят кейсы и агрегаты, экономика \
(ожидаемое снижение, наша конкурентность), одним абзацем до ~120 слов;
- risks — до 4 конкретных рисков (каждый ≤ 10 слов);
- participate — «да» / «нет» / «на грани»;
- expected_reduction_min_pct / expected_reduction_max_pct — ожидаемый диапазон снижения \
цены победителя от НМЦ, в процентах (числа; из фактов, не из головы);
- recommended_bid — ценовая стратегия одной фразой (напр. «подавать на 12–15% ниже НМЦ \
≈ 8.1–8.4 млн ₽»); если участвовать не стоит — почему коротко;
- confidence — 0–100, насколько уверен (мало кейсов/нет участий -> ниже);
- data_notes — на каких данных построен вывод и каких данных не хватает."""
)

RECO_SCHEMA: dict[str, Any] = {
    "type": "object",
    "properties": {
        "rationale": {"type": "string"},
        "risks": {"type": "array", "items": {"type": "string"}},
        "participate": {"type": "string", "enum": ["да", "нет", "на грани"]},
        "expected_reduction_min_pct": {"type": "number"},
        "expected_reduction_max_pct": {"type": "number"},
        "recommended_bid": {"type": "string"},
        "confidence": {"type": "integer"},
        "data_notes": {"type": "string"},
    },
    "required": [
        "rationale",
        "risks",
        "participate",
        "expected_reduction_min_pct",
        "expected_reduction_max_pct",
        "recommended_bid",
        "confidence",
        "data_notes",
    ],
    "additionalProperties": False,
}


def build_message(
    target_block: str, aggregates_block: str, cases_block: str, feedback_block: str
) -> str:
    parts = [
        "=== ЦЕЛЕВАЯ ЗАКУПКА ===",
        target_block,
        "",
        "=== ФАКТЫ ПО СЕГМЕНТУ (посчитано по базе бюро) ===",
        aggregates_block or "Данных по сегменту нет.",
    ]
    if cases_block:
        parts += ["", "=== ПОХОЖИЕ КЕЙСЫ ИЗ ИСТОРИИ БЮРО ===", cases_block]
    else:
        parts += ["", "=== ПОХОЖИЕ КЕЙСЫ ===", "Похожих кейсов в базе нет."]
    if feedback_block:
        parts += [
            "",
            "=== КОНТРПРИМЕРЫ (прошлые рекомендации с оценкой «мимо») ===",
            feedback_block,
        ]
    parts += ["", "Дай рекомендацию по схеме."]
    return "\n".join(parts)
