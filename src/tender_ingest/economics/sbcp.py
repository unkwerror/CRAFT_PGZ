"""СБЦП — нормативные веса разделов ПД/РД (второй источник долей после аналогов).

Источник: государственный сметный норматив СБЦП 81-2001-03 «Объекты
жилищно-гражданского строительства» (приказ Минрегиона России от 28.05.2010 № 260):
- таблица № 41 — относительная стоимость разделов ПРОЕКТНОЙ документации (сумма 100%),
  подразделы ИОС — «К таблице № 41» (в сумме 37%);
- таблица № 42 — то же для РАБОЧЕЙ документации (сумма 100%), подразделы «К таблице № 42»;
- п. 1.5 — распределение базовой цены: ПД 40% / РД 60% (используется для смеси ПД+РД).
Данные сверены с полным текстом справочника (meganorm, идентичен docs.cntd 1200135086).

ВАЖНО: веса — ОТНОСИТЕЛЬНЫЕ. Абсолютные суммы бюро им не равны (в цене контракта
сидит прибыль, у бюро свой уровень субподряда), поэтому engine приводит веса к уровню
затрат бюро коэффициентом k = медиана(доля_по_аналогам / вес_СБЦП) по разделам,
у которых есть И аналоги, И вес СБЦП — см. engine.apply_sbcp_fallback.

Справочник охват — здания (жилищно-гражданское строительство): применяется только
когда ИИ определил object_kind='building'. Для благоустройства/ОКН нормативной
таблицы в этом модуле пока нет — такие разделы честно остаются «нет данных».
"""

from __future__ import annotations

from tender_ingest.economics.canon import COMPOSITE_SECTIONS

SBCP_SOURCE = (
    "СБЦП «Объекты жилищно-гражданского строительства» "
    "(приказ Минрегиона России от 28.05.2010 № 260), табл. 41/42"
)

# Таблица № 41 (проектная документация), % — сумма 100.
_PD_WEIGHTS: dict[str, float] = {
    "pz": 2.0,
    "pzu": 4.0,
    "ar": 14.0,
    "kr": 15.0,
    # ИОС (37% суммарно) — разбивка «К таблице № 41»
    "ios1": 7.0,  # электроснабжение
    "ios2": 4.0,  # водоснабжение
    "ios3": 4.0,  # водоотведение
    "ios4": 12.0,  # отопление, вентиляция, кондиционирование
    "ios5": 3.0,  # связь
    "ios6": 2.0,  # газоснабжение
    "tx": 5.0,  # технологические решения
    "pos": 6.0,
    "oos": 7.0,
    "pb": 6.0,
    "odi": 2.0,
    "smeta": 7.0,
}

# Таблица № 42 (рабочая документация), % — сумма 100. ПЗ/ПОС/ПОД/ООС в РД не разрабатываются.
_RD_WEIGHTS: dict[str, float] = {
    "pzu": 1.0,
    "ar": 22.0,
    "kr": 27.0,
    # ИОС (34% суммарно) — разбивка «К таблице № 42»
    "ios1": 5.0,
    "ios2": 3.0,
    "ios3": 3.0,
    "ios4": 14.0,
    "ios5": 3.0,
    "ios6": 2.0,
    "tx": 4.0,
    "pb": 4.0,
    "odi": 3.0,
    "smeta": 9.0,
}

# п. 1.5 СБЦП: базовая цена = ПД 40% + РД 60%.
_PD_SHARE_OF_BASE = 0.4
_RD_SHARE_OF_BASE = 0.6

DESIGN_STAGES = ("pd", "rd", "pd_rd")


def sbcp_weights(design_stage: str) -> dict[str, float]:
    """Нормированные веса разделов (сумма 1.0) для стадии: pd | rd | pd_rd (смесь 40/60)."""
    if design_stage == "pd":
        raw = dict(_PD_WEIGHTS)
    elif design_stage == "rd":
        raw = dict(_RD_WEIGHTS)
    else:  # pd_rd и всё неизвестное — консервативная смесь по п. 1.5
        keys = set(_PD_WEIGHTS) | set(_RD_WEIGHTS)
        raw = {
            key: _PD_SHARE_OF_BASE * _PD_WEIGHTS.get(key, 0.0)
            + _RD_SHARE_OF_BASE * _RD_WEIGHTS.get(key, 0.0)
            for key in keys
        }
    total = sum(raw.values())
    weights = {key: value / total for key, value in raw.items() if value > 0}
    for composite, parts in COMPOSITE_SECTIONS.items():
        value = sum(weights.get(p, 0.0) for p in parts)
        if value > 0:
            weights[composite] = value
    return weights


def stage_label(design_stage: str) -> str:
    return {"pd": "ПД", "rd": "РД", "pd_rd": "ПД+РД (40/60)"}.get(design_stage, design_stage)
